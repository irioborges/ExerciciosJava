package asp02;

public class Area {

  public static double meterToFeet(double area){
    return area * 10.76;  
  }
  
  public static double feetToCentimeter(double area){
    return area * 929;  
  }
  
  public static double milhaToAcre(double area){
    return area * 640;  
  }
 
  public static double acreToFeet(double area){
    return area * 43.650;  
  }
  
}
