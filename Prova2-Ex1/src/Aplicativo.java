    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      try (Scanner input = new Scanner(new File("/home/jTextArea.txt"))) {
              while (input.hasNext()) {
                  String linha = input.nextLine();
                  jTextArea1.setText(jTextArea1.getText() + linha);
              }
          }
      catch(FileNotFoundException e) {
        System.err.println("Arquivo não encontrado!"); 
      }    
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      File f;
      f = new File("/home/jTextArea.txt");
      if(f.exists() == false){
          try{
            f.createNewFile();
          }catch(Exception e){
              System.out.println("" + e);
          }
      }
      try (Formatter output = new Formatter("/home/jTextArea.txt")) {
            output.format(jTextArea1.getText());
            output.close();
      }catch(Exception e) {
        System.err.println("Exceção: " + e); 
      }              
    }//GEN-LAST:event_jButton1ActionPerformed

